package exercicio2;

public class Livro {
	
	private String nome;
	private String autor;
	private String isbn;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getAutor() {
		return autor;
	}
	public void setAutor(String autor) {
		this.autor = autor;
	}
	public String getIsbn() {
		return isbn;
	}
	public void setIsbn(String iSBN) {
		isbn = iSBN;
	}
	

}
